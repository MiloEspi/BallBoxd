export const translate = (
  language: Language,
  key: string,
  vars?: Record<string, string | number>,
) => {
  const template = translations[language][key] ?? key;
  if (!vars) {
    return template;
  }
  return Object.entries(vars).reduce(
    (acc, [name, value]) =>
      acc.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value)),
    template,
  );
};

export const getRelativeLabel = (
  language: Language,
  diffDays: number,
  weekday: string,
) => {
  if (diffDays === 0) {
    return translate(language, 'date.today');
  }
  if (diffDays === -1) {
    return translate(language, 'date.yesterday');
  }
  if (diffDays === 1) {
    return translate(language, 'date.tomorrow');
  }
  return weekday;
};
